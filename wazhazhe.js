questionNumber = 0
lesson = ""


function vocab(le){
    lesson = le
    start.style.display = "none"
    button.style.display = "block"
    vocabulary.style.display = "block"
    vocabulary.innerHTML = lessons[lesson].vocab.map(x => `<tr><td>${x.split(": ")[0]}</td><td>${x.split(": ")[1]}</td></tr>`).join("")
}

function read(le){
    lesson = le
    start.style.display = "none"
    button.style.display = "block"
    grammar.style.display = "block"
    grammar.textContent = lessons[lesson].grammar
}
function generateQuestion(){
    myquestion = lessons[lesson].questions[questionNumber]
    console.log(myquestion)
    if(myquestion.startsWith("Write") || myquestion.startsWith("Translate")){
        alert('h')
        question.textContent = myquestion.split(" | ")[0]
        write.style.display = "block"
        click.style.display = "none"
    }
    else{
        alert('hhhh')
        question.textContent = myquestion.split(" | ")[0]
        click.style.display = "block"
        write.style.display = "none"
        answers = myquestion.split(" | ")[1].split(";")
        b0.textContent = answers[0]
        b1.textContent = answers[1]
        b2.textContent = answers[2]
    }
}

function next(){
    questionNumber++
    if(questionNumber < lessons[lesson].questions.length){
        generateQuestion()
    }
    else menu()
}

function zone(le){
    lesson = le
    start.style.display = "none"
    button.style.display = "block"
    quiz.style.display = "block"
    questionNumber = 0
    generateQuestion()
}
function menu(){
    vocabulary.style.display = "none"
    grammar.style.display = "none"
    quiz.style.display = "none"
    start.style.display = "block"
    button.style.display = "none"
}